// Incrementos

let puntaje = 10;

// Incremento en 1 ++
puntaje++; //Empienza desde 11
puntaje += 0; // Empieza sumando el número que lo acompaña 
                // Muy usado para la suma de likes o resta de inventarios

// Decremento en 1 --
--puntaje; //empieza desde 10

console.log(puntaje);